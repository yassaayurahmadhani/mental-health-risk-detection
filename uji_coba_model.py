class HybridSampler:
    """
    Kelas untuk melakukan Hybrid Sampling (Under-sampling + Over-sampling).

    Strategi:
    1. Under-sampling: Kurangi kelas mayoritas ke target count
    2. Over-sampling: Tambah kelas minoritas ke target count (dengan random duplication)
    """

    def __init__(self, augmenter=None):
        """
        Args:
            augmenter: BackTranslationAugmenter instance untuk augmentasi (opsional)
        """
        self.augmenter = augmenter

    def balance_dataset(self, df, target_column='sentiment_category',
                        target_count=None, use_back_translation=False,
                        text_column='processed_text'):
        """
        Balance dataset menggunakan hybrid sampling.

        Args:
            df: DataFrame original
            target_column: Kolom target (label)
            target_count: Target jumlah per kelas (None = median)
            use_back_translation: Gunakan back translation untuk over-sampling
            text_column: Kolom teks untuk back translation

        Returns:
            DataFrame: Balanced dataset
        """
        print("\n" + "="*60)
        print("HYBRID SAMPLING - BALANCING DATASET")
        print("="*60)

        # Hitung distribusi awal
        class_counts = df[target_column].value_counts()
        print(f"\nDistribusi SEBELUM balancing:")
        for cls, cnt in class_counts.items():
            print(f"  • {cls}: {cnt:,}")

        # Tentukan target count
        if target_count is None:
            # Gunakan nilai median sebagai target
            target_count = int(class_counts.median())

        print(f"\n🎯 Target count per kelas: {target_count:,}")

        balanced_dfs = []

        for class_name in class_counts.index:
            class_df = df[df[target_column] == class_name].copy()
            current_count = len(class_df)

            if current_count > target_count:
                # UNDER-SAMPLING: Kurangi kelas mayoritas
                print(f"\n[UNDER-SAMPLING] {class_name}: {current_count:,} → {target_count:,}")
                sampled_df = class_df.sample(n=target_count, random_state=42)
                sampled_df['sampling_method'] = 'undersampled'
                balanced_dfs.append(sampled_df)

            elif current_count < target_count:
                # OVER-SAMPLING: Tambah kelas minoritas
                need_more = target_count - current_count
                print(f"\n[OVER-SAMPLING] {class_name}: {current_count:,} → {target_count:,} (+{need_more:,})")

                if use_back_translation and self.augmenter and BACK_TRANSLATION_AVAILABLE:
                    # Gunakan back translation untuk augmentasi
                    print("  → Menggunakan Back Translation...")
                    class_df['sampling_method'] = 'original'

                    # Sample untuk di-augment
                    samples_to_augment = class_df.sample(n=need_more, replace=True, random_state=42)
                    augmented_rows = []

                    for idx, row in tqdm(samples_to_augment.iterrows(),
                                        total=len(samples_to_augment),
                                        desc=f"  Augmenting {class_name}"):
                        new_row = row.copy()
                        # Coba back translation, jika gagal duplikat saja
                        new_text = self.augmenter.back_translate(row[text_column], delay=0.3)
                        new_row[text_column] = new_text if new_text else row[text_column]
                        new_row['sampling_method'] = 'augmented_bt'
                        augmented_rows.append(new_row)

                    augmented_df = pd.DataFrame(augmented_rows)
                    combined_df = pd.concat([class_df, augmented_df], ignore_index=True)
                    balanced_dfs.append(combined_df)
                else:
                    # Random duplication (fallback)
                    print("  → Menggunakan Random Duplication...")
                    class_df['sampling_method'] = 'original'

                    # Random sample dengan replacement
                    extra_samples = class_df.sample(n=need_more, replace=True, random_state=42).copy()
                    extra_samples['sampling_method'] = 'duplicated'

                    combined_df = pd.concat([class_df, extra_samples], ignore_index=True)
                    balanced_dfs.append(combined_df)
            else:
                # Tepat sama dengan target
                print(f"\n[OK] {class_name}: {current_count:,} (sudah seimbang)")
                class_df['sampling_method'] = 'original'
                balanced_dfs.append(class_df)

        # Gabungkan semua kelas
        balanced_df = pd.concat(balanced_dfs, ignore_index=True)

        # Shuffle data
        balanced_df = balanced_df.sample(frac=1, random_state=42).reset_index(drop=True)

        # Report hasil
        print("\n" + "-"*40)
        print("HASIL HYBRID SAMPLING:")
        print("-"*40)
        final_dist = balanced_df[target_column].value_counts()
        for cls, cnt in final_dist.items():
            print(f"  • {cls}: {cnt:,}")
        print(f"\n✓ Total data setelah balancing: {len(balanced_df):,}")
        print(f"  • Original: {len(balanced_df[balanced_df['sampling_method'] == 'original']):,}")
        print(f"  • Undersampled: {len(balanced_df[balanced_df['sampling_method'] == 'undersampled']):,}")
        print(f"  • Duplicated: {len(balanced_df[balanced_df['sampling_method'] == 'duplicated']):,}")
        if 'augmented_bt' in balanced_df['sampling_method'].values:
            print(f"  • Augmented (BT): {len(balanced_df[balanced_df['sampling_method'] == 'augmented_bt']):,}")

        return balanced_df


