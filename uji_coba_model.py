class ImprovedLSTMClassifier:
    """
    LSTM Classifier untuk klasifikasi risiko kesehatan mental.
    Dioptimalkan untuk data balanced.
    """

    def __init__(self, output_dir='MentalTrendAI_Output'):
        self.output_dir = output_dir
        self.model_dir = os.path.join(output_dir, 'models')
        self.viz_dir = os.path.join(output_dir, 'visualizations')
        self.eval_dir = os.path.join(output_dir, 'evaluations')

        os.makedirs(self.model_dir, exist_ok=True)
        os.makedirs(self.viz_dir, exist_ok=True)
        os.makedirs(self.eval_dir, exist_ok=True)

        self.model = None
        self.scaler = MinMaxScaler()
        self.label_encoder = LabelEncoder()
        self.history = None

    def prepare_features(self, df, target_column='sentiment_category'):
        """
        Prepare features untuk LSTM training.
        Mengekstrak fitur dari data teks dan temporal.
        """
        print("\n📊 Preparing features...")

        # Encode target
        df['target_encoded'] = self.label_encoder.fit_transform(df[target_column])

        # Feature engineering
        features = []

        # 1. Sentiment score (jika ada)
        if 'sentiment_score' in df.columns:
            features.append('sentiment_score')

        # 2. Temporal features (jika ada)
        if 'hour' in df.columns:
            features.append('hour')
        if 'weekday' in df.columns:
            features.append('weekday')
        if 'month' in df.columns:
            features.append('month')

        # 3. Text length feature
        if 'processed_text' in df.columns:
            df['text_length'] = df['processed_text'].apply(lambda x: len(str(x).split()) if pd.notna(x) else 0)
            features.append('text_length')

        # 4. Sentiment numeric (convert dari label)
        if 'sentiment' in df.columns:
            features.append('sentiment')

        # Jika tidak ada fitur cukup, buat fitur dari text
        if len(features) < 3:
            print("  → Membuat fitur tambahan dari teks...")
            df['word_count'] = df['processed_text'].apply(lambda x: len(str(x).split()) if pd.notna(x) else 0)
            df['char_count'] = df['processed_text'].apply(lambda x: len(str(x)) if pd.notna(x) else 0)
            features.extend(['word_count', 'char_count'])

        print(f"  ✓ Features used: {features}")

        X = df[features].fillna(0).values
        y = df['target_encoded'].values

        # Scale features
        X_scaled = self.scaler.fit_transform(X)

        return X_scaled, y, features

    def build_model(self, input_shape, num_classes):
        """
        Build LSTM model architecture.
        """
        print(f"\n🏗️ Building LSTM model...")
        print(f"  Input shape: {input_shape}")
        print(f"  Output classes: {num_classes}")

        model = Sequential([
            # Reshape untuk LSTM (samples, timesteps, features)
            keras.layers.Reshape((1, input_shape), input_shape=(input_shape,)),

            # LSTM Layer 1 - Bidirectional
            Bidirectional(LSTM(
                LSTM_CONFIG['LSTM_UNITS'][0],
                return_sequences=True,
                kernel_regularizer=l2(0.0001)
            )),
            BatchNormalization(),
            Dropout(LSTM_CONFIG['DROPOUT_RATES'][0]),

            # LSTM Layer 2 - Bidirectional
            Bidirectional(LSTM(
                LSTM_CONFIG['LSTM_UNITS'][1],
                return_sequences=True,
                kernel_regularizer=l2(0.0001)
            )),
            BatchNormalization(),
            Dropout(LSTM_CONFIG['DROPOUT_RATES'][1]),

            # LSTM Layer 3
            LSTM(
                LSTM_CONFIG['LSTM_UNITS'][2],
                return_sequences=False,
                kernel_regularizer=l2(0.0001)
            ),
            BatchNormalization(),
            Dropout(LSTM_CONFIG['DROPOUT_RATES'][2]),

            # Dense layers
            Dense(64, activation='relu'),
            Dropout(0.1),
            Dense(32, activation='relu'),

            # Output layer - Classification
            Dense(num_classes, activation='softmax')
        ])

        # Compile dengan class weights untuk handle remaining imbalance
        model.compile(
            optimizer=Adam(learning_rate=LSTM_CONFIG['LEARNING_RATE']),
            loss='sparse_categorical_crossentropy',
            metrics=['accuracy']
        )

        model.summary()
        return model

    def train(self, X_train, y_train, X_val, y_val):
        """
        Train LSTM model.
        """
        print("\n🚀 Training LSTM model...")

        # Hitung class weights untuk handle slight imbalance
        class_counts = np.bincount(y_train)
        total = len(y_train)
        class_weights = {i: total / (len(class_counts) * count) for i, count in enumerate(class_counts)}
        print(f"  Class weights: {class_weights}")

        # Callbacks
        callbacks = [
            keras.callbacks.EarlyStopping(
                monitor='val_loss',
                patience=LSTM_CONFIG['EARLY_STOPPING_PATIENCE'],
                restore_best_weights=True,
                verbose=1
            ),
            keras.callbacks.ReduceLROnPlateau(
                monitor='val_loss',
                factor=0.5,
                patience=LSTM_CONFIG['REDUCE_LR_PATIENCE'],
                min_lr=1e-6,
                verbose=1
            )
        ]

        # Build model
        input_shape = X_train.shape[1]
        num_classes = len(np.unique(y_train))
        self.model = self.build_model(input_shape, num_classes)

        # Training
        self.history = self.model.fit(
            X_train, y_train,
            validation_data=(X_val, y_val),
            epochs=LSTM_CONFIG['MAX_EPOCHS'],
            batch_size=LSTM_CONFIG['BATCH_SIZE'],
            class_weight=class_weights,
            callbacks=callbacks,
            verbose=1
        )

        return self.history

    def evaluate(self, X_test, y_test):
        """
        Evaluate model dan buat confusion matrix.
        """
        print("\n📈 Evaluating model...")

        # Predictions
        y_pred_proba = self.model.predict(X_test, verbose=0)
        y_pred = np.argmax(y_pred_proba, axis=1)

        # Metrics
        accuracy = accuracy_score(y_test, y_pred)
        precision = precision_score(y_test, y_pred, average='weighted')
        recall = recall_score(y_test, y_pred, average='weighted')
        f1 = f1_score(y_test, y_pred, average='weighted')

        print(f"\n✓ HASIL EVALUASI:")
        print(f"  • Accuracy:  {accuracy:.4f} ({accuracy*100:.2f}%)")
        print(f"  • Precision: {precision:.4f}")
        print(f"  • Recall:    {recall:.4f}")
        print(f"  • F1-Score:  {f1:.4f}")

        # Classification Report
        class_names = self.label_encoder.classes_
        print(f"\n📋 Classification Report:")
        print(classification_report(y_test, y_pred, target_names=class_names))

        # Confusion Matrix
        cm = confusion_matrix(y_test, y_pred)

        return {
            'accuracy': accuracy,
            'precision': precision,
            'recall': recall,
            'f1_score': f1,
            'confusion_matrix': cm,
            'y_true': y_test,
            'y_pred': y_pred,
            'class_names': class_names
        }

