import time

class BackTranslationAugmenter:
    """
    Kelas untuk melakukan augmentasi data teks menggunakan Back Translation.

    Alur:
    1. Teks Indonesia → Translate ke Inggris
    2. Teks Inggris → Translate balik ke Indonesia

    Hasilnya adalah variasi kalimat baru dengan makna serupa.
    """

    def __init__(self):
        if BACK_TRANSLATION_AVAILABLE:
            self.translator_id_to_en = GoogleTranslator(source='id', target='en')
            self.translator_en_to_id = GoogleTranslator(source='en', target='id')
        else:
            self.translator_id_to_en = None
            self.translator_en_to_id = None

    def back_translate(self, text, delay=0.5):
        """
        Melakukan back translation pada satu teks.

        Args:
            text: Teks asli dalam bahasa Indonesia
            delay: Delay antar request untuk menghindari rate limiting

        Returns:
            str: Teks hasil back translation (variasi baru)
        """
        if not BACK_TRANSLATION_AVAILABLE or not text or len(text.strip()) < 5:
            return text

        try:
            # Step 1: Translate ID → EN
            english_text = self.translator_id_to_en.translate(text)
            time.sleep(delay)

            # Step 2: Translate EN → ID (back translation)
            back_translated = self.translator_en_to_id.translate(english_text)
            time.sleep(delay)

            return back_translated
        except Exception as e:
            # Jika gagal, kembalikan teks asli
            return text

    def augment_dataframe(self, df, text_column='processed_text', target_column='sentiment_category',
                          minority_classes=None, target_count=None, max_augment_per_class=1000):
        """
        Augmentasi DataFrame dengan back translation untuk kelas minoritas.

        Args:
            df: DataFrame original
            text_column: Nama kolom teks yang akan di-augment
            target_column: Nama kolom target (label)
            minority_classes: List kelas yang akan di-augment (jika None, deteksi otomatis)
            target_count: Target jumlah data per kelas (jika None, samakan dengan mayoritas)
            max_augment_per_class: Maksimum augmentasi per kelas

        Returns:
            DataFrame: DataFrame dengan data augmented
        """
        print("\n" + "="*60)
        print("BACK TRANSLATION - DATA AUGMENTATION")
        print("="*60)

        # Hitung distribusi
        class_counts = df[target_column].value_counts()
        print(f"Distribusi awal:\n{class_counts}\n")

        # Tentukan kelas minoritas jika tidak disediakan
        if minority_classes is None:
            # Anggap semua kelas dengan count < 80% dari max adalah minoritas
            max_count = class_counts.max()
            minority_classes = class_counts[class_counts < max_count * 0.8].index.tolist()

        if not minority_classes:
            print("✓ Tidak ada kelas minoritas yang perlu di-augment")
            return df

        print(f"Kelas minoritas yang akan di-augment: {minority_classes}")

        # Target count (default: samakan dengan 80% mayoritas)
        if target_count is None:
            target_count = int(class_counts.max() * 0.8)

        print(f"Target count per kelas: {target_count:,}")

        augmented_rows = []

        for minority_class in minority_classes:
            current_count = class_counts[minority_class]
            need_augment = min(target_count - current_count, max_augment_per_class)

            if need_augment <= 0:
                continue

            print(f"\nMengaugment kelas '{minority_class}'...")
            print(f"  Current: {current_count:,}, Need: {need_augment:,}")

            # Ambil data dari kelas minoritas
            minority_data = df[df[target_column] == minority_class]

            # Random sample untuk di-augment (bisa repeat jika data sedikit)
            samples = minority_data.sample(n=need_augment, replace=True, random_state=42)

            augmented_count = 0
            for idx, row in tqdm(samples.iterrows(), total=len(samples), desc=f"  Augmenting {minority_class}"):
                original_text = row[text_column]

                # Back translate
                augmented_text = self.back_translate(original_text)

                # Jika hasil berbeda dari aslinya, tambahkan
                if augmented_text and augmented_text != original_text:
                    new_row = row.copy()
                    new_row[text_column] = augmented_text
                    new_row['is_augmented'] = True  # Tandai sebagai augmented
                    augmented_rows.append(new_row)
                    augmented_count += 1

            print(f"  ✓ Berhasil augment {augmented_count:,} data untuk '{minority_class}'")

        # Gabungkan dengan data asli
        if augmented_rows:
            df_augmented = pd.concat([df, pd.DataFrame(augmented_rows)], ignore_index=True)
            df_augmented['is_augmented'] = df_augmented['is_augmented'].fillna(False)

            print(f"\n✓ Total data setelah augmentasi: {len(df_augmented):,}")
            print(f"  Data asli: {len(df):,}")
            print(f"  Data augmented: {len(augmented_rows):,}")

            return df_augmented
        else:
            df['is_augmented'] = False
            return df


