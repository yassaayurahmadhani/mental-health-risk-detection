    def process_ppkm_raw(self,
                        df_raw: pd.DataFrame,
                        df_labeled: pd.DataFrame = None,
                        output_filename='INA_TweetsPPKM_MentalHealth_Filtered.csv',
                        use_labeled_sentiment=True):
        """
        Proses lengkap: filter dan convert PPKM dataset.

        PERBAIKAN: Juga memproses INA_TweetsPPKM_Labeled_Pure.csv untuk:
        1. Menggunakan label sentiment asli yang lebih akurat
        2. Mendapatkan distribusi sentiment yang lebih seimbang

        Args:
            df_raw (pd.DataFrame): DataFrame dari INA_TweetsPPKM_Raw.csv
            df_labeled (pd.DataFrame): DataFrame dari INA_TweetsPPKM_Labeled_Pure.csv (optional)
            output_filename (str): Nama file output
            use_labeled_sentiment (bool): Jika True, gunakan sentiment dari labeled file

        Returns:
            pd.DataFrame: DataFrame yang sudah diproses
        """
        print("\n" + "="*60)
        print("TAHAP 1: FILTERING MENTAL HEALTH CONTENT")
        print("="*60)

        # 1. Initialize Mental Health Filter
        print("\nInisialisasi Mental Health Filter...")
        mh_filter = MentalHealthFilter(
            threshold_keyword=1,
            threshold_contextual=0.60  # Sedikit lebih rendah untuk PPKM dataset
        )

        # 2. Load original data (Raw - tanpa sentiment)
        print(f"\nData Raw dimuat: {len(df_raw):,} baris")
        df_original = df_raw.copy()

        # 2b. PERBAIKAN: Load labeled data untuk sentiment
        if df_labeled is not None and use_labeled_sentiment:
            print(f"\nData Labeled dimuat: {len(df_labeled):,} baris")

            # Tampilkan distribusi sentiment asli
            print("\nDistribusi Sentiment Asli (dari Labeled data):")
            sent_dist = df_labeled['sentiment'].value_counts()
            total = len(df_labeled)
            for sent_id, count in sent_dist.items():
                label = SENTIMENT_MAP.get(sent_id, f'Unknown({sent_id})')
                print(f"  {label}: {count:,} ({count/total*100:.2f}%)")

        # 3. Filter mental health content
        df_filtered = mh_filter.filter_mental_health_content(df_original)

        # 3b. PERBAIKAN: Merge sentiment labels dari labeled data
        if df_labeled is not None and use_labeled_sentiment:
            print("\nMenggabungkan sentiment labels dari data labeled...")

            # Merge berdasarkan Tweet text (karena mungkin tidak ada unique ID)
            # PERBAIKAN: Gunakan kombinasi Date + User + Tweet untuk matching yang lebih akurat
            if 'Tweet' in df_filtered.columns and 'Tweet' in df_labeled.columns:
                # Coba merge dengan multiple keys jika tersedia
                if all(col in df_filtered.columns and col in df_labeled.columns for col in ['Date', 'User']):
                    # Merge menggunakan Date + User + Tweet sebagai composite key
                    df_merged = df_filtered.merge(
                        df_labeled[['Date', 'User', 'Tweet', 'sentiment']],
                        on=['Date', 'User', 'Tweet'],
                        how='left',
                        suffixes=('', '_labeled')
                    )
                    df_filtered['sentiment'] = df_merged['sentiment']
                    print("  Menggunakan composite key (Date + User + Tweet) untuk matching")
                else:
                    # Fallback: Create a mapping from Tweet to sentiment
                    tweet_to_sentiment = dict(zip(df_labeled['Tweet'], df_labeled['sentiment']))
                    df_filtered['sentiment'] = df_filtered['Tweet'].map(tweet_to_sentiment)
                    print("  Menggunakan Tweet text saja untuk matching (mungkin kurang akurat)")

                # Hitung matched SEBELUM fillna
                matched = df_filtered['sentiment'].notna().sum()
                unmatched = len(df_filtered) - matched

                # Fill missing dengan DEFAULT_SENTIMENT (netral) sebagai default
                df_filtered['sentiment'] = df_filtered['sentiment'].fillna(DEFAULT_SENTIMENT).astype(int)

                print(f"✓ {matched:,} dari {len(df_filtered):,} tweets berhasil di-match dengan sentiment labels")
                if unmatched > 0:
                    print(f"  ⚠ {unmatched:,} tweets tidak memiliki label, menggunakan default Netral")

                # Tampilkan distribusi sentiment hasil filter
                print("\nDistribusi Sentiment (setelah mental health filtering):")
                sent_dist = df_filtered['sentiment'].value_counts()
                total_filtered = len(df_filtered)
                for sent_id, count in sent_dist.items():
                    label = SENTIMENT_MAP.get(sent_id, f'Unknown({sent_id})')
                    print(f"  {label}: {count:,} ({count/total_filtered*100:.2f}%)")

        # 4. Save filtered data
        output_path = os.path.join(self.data_dir, output_filename)
        mh_filter.save_filtered_data(df_filtered, output_path)

        # 5. Generate filter report
        report_path = os.path.join(self.data_dir, 'mental_health_filter_report.json')
        mh_filter.generate_filter_report(df_original, df_filtered, report_path)

        print("\n" + "="*60)
        print("TAHAP 2: FORMAT CONVERSION")
        print("="*60)

        # 6. Convert to pipeline-compatible format
        df_converted = self.convert_to_pipeline_format(df_filtered)

        # Save converted format
        converted_path = os.path.join(self.data_dir,
                                     output_filename.replace('.csv', '_pipeline_ready.csv'))
        df_converted.to_csv(converted_path, index=False)
        print(f"\n✓ Data pipeline-ready disimpan ke {converted_path}")

        print("\n" + "="*60)
        print("TAHAP 3: VALIDASI DAN SAMPLING")
        print("="*60)

        # 7. Generate sample untuk verifikasi manual
        self.generate_sample_for_verification(df_filtered)

        # 8. Generate preprocessing report
        self.generate_preprocessing_report(df_original, df_filtered, df_converted)

        return df_converted

    def convert_to_pipeline_format(self, df: pd.DataFrame) -> pd.DataFrame:
        """
        Konversi format PPKM ke format yang compatible dengan pipeline.

        Pipeline expects:
            - post_created: Timestamp
            - post_text: Teks konten
            - (optional) label: 0 atau 1

        Args:
            df (pd.DataFrame): DataFrame PPKM yang sudah difilter

        Returns:
            pd.DataFrame: DataFrame dalam format pipeline
        """
        print("\nMengkonversi format dataset...")

        df_converted = pd.DataFrame()

        # Map kolom Date -> post_created
        if 'Date' in df.columns:
            df_converted['post_created'] = df['Date']
        elif 'date' in df.columns:
            df_converted['post_created'] = df['date']
        else:
            print("PERINGATAN: Kolom Date tidak ditemukan, menggunakan timestamp sekarang")
            df_converted['post_created'] = datetime.now()

        # Map kolom Tweet -> post_text
        if 'Tweet' in df.columns:
            df_converted['post_text'] = df['Tweet']
        elif 'tweet' in df.columns:
            df_converted['post_text'] = df['tweet']
        elif 'text' in df.columns:
            df_converted['post_text'] = df['text']
        else:
            print("ERROR: Kolom Tweet/text tidak ditemukan!")
            return None

        # Add optional columns if available
        if 'User' in df.columns:
            df_converted['user_id'] = df['User']

        # Add mental health metadata
        if 'mh_category' in df.columns:
            df_converted['mh_category'] = df['mh_category']
            df_converted['mh_confidence'] = df['mh_confidence']
            df_converted['mh_matched_keywords'] = df['mh_matched_keywords']

        # =====================================================================
        # PERBAIKAN: Add subject demographics untuk mencegah duplikasi di pipeline
        # Kolom ini dihasilkan oleh analyze_subject_category() di mental_health_filter
        # =====================================================================
        if 'subject_category' in df.columns:
            df_converted['subject_category'] = df['subject_category']
            print("✓ Subject category (demographics) disertakan")
        if 'subject_confidence' in df.columns:
            df_converted['subject_confidence'] = df['subject_confidence']
        if 'subject_details' in df.columns:
            df_converted['subject_details'] = df['subject_details']

        # PERBAIKAN: Add sentiment labels jika tersedia dari labeled data
        if 'sentiment' in df.columns:
            df_converted['sentiment'] = df['sentiment']
            print("✓ Sentiment labels dari data labeled disertakan")

            # Tampilkan distribusi
            sent_dist = df_converted['sentiment'].value_counts()
            print("  Distribusi sentiment:")
            for sent_id, count in sent_dist.items():
                label = SENTIMENT_MAP.get(sent_id, f'Unknown({sent_id})')
                print(f"    {label}: {count:,}")
        else:
            # Add dummy label (akan diganti dengan sentiment dari BERT)
            # 1 = positive, 0 = negative (placeholder)
            df_converted['label'] = 1
            print("⚠ Sentiment labels tidak tersedia, menggunakan label dummy")

        print(f"✓ Format dikonversi: {len(df_converted):,} baris, {len(df_converted.columns)} kolom")
        print(f"  Kolom: {df_converted.columns.tolist()}")

        return df_converted

    def generate_sample_for_verification(self, df: pd.DataFrame, n_samples=20):
        """
        Generate sample tweets untuk verifikasi manual.

        Args:
            df (pd.DataFrame): DataFrame yang sudah difilter
            n_samples (int): Jumlah sample per kategori
        """
        print(f"\nMembuat sample untuk verifikasi manual ({n_samples} per kategori)...")

        sample_path = os.path.join(self.data_dir, 'sample_filtered_tweets.txt')

        with open(sample_path, 'w', encoding='utf-8') as f:
            f.write("="*80 + "\n")
            f.write("SAMPLE TWEETS UNTUK VERIFIKASI MANUAL\n")
            f.write(f"Timestamp: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
            f.write("="*80 + "\n\n")

            # Sample per kategori
            if 'mh_category' in df.columns:
                categories = df['mh_category'].value_counts().index.tolist()

                for category in categories:
                    category_df = df[df['mh_category'] == category]
                    sample_df = category_df.sample(min(n_samples, len(category_df)))

                    f.write(f"\n{'='*80}\n")
                    f.write(f"KATEGORI: {category.upper()}\n")
                    f.write(f"Total: {len(category_df)} tweets\n")
                    f.write(f"{'='*80}\n\n")

                    for seq_num, (idx, row) in enumerate(sample_df.iterrows(), 1):
                        text_col = 'Tweet' if 'Tweet' in row else 'text'
                        f.write(f"{seq_num}. {row[text_col]}\n")
                        f.write(f"   Keywords: {row.get('mh_matched_keywords', 'N/A')}\n")
                        f.write(f"   Confidence: {row.get('mh_confidence', 'N/A')}\n")

                        # Format contextual score safely
                        score = row.get('mh_contextual_score', 0)
                        if isinstance(score, (int, float)):
                            f.write(f"   Contextual Score: {score:.3f}\n")
                        else:
                            f.write(f"   Contextual Score: N/A\n")
                        f.write("\n")

        print(f"✓ Sample disimpan ke {sample_path}")
        print("  Silakan review manual untuk memverifikasi kualitas filtering.")

    def generate_preprocessing_report(self, df_original, df_filtered, df_converted):
        """
        Generate comprehensive preprocessing report.

        Args:
            df_original (pd.DataFrame): Data asli
            df_filtered (pd.DataFrame): Data yang sudah difilter
            df_converted (pd.DataFrame): Data yang sudah dikonversi
        """
        print("\nMembuat laporan preprocessing...")

        report = {
            'metadata': {
                'timestamp': datetime.now().isoformat(),
                'input_file': 'INA_TweetsPPKM_Raw.csv',
                'output_file': 'INA_TweetsPPKM_MentalHealth_Filtered_pipeline_ready.csv'
            },
            'statistics': {
                'original_rows': len(df_original),
                'filtered_rows': len(df_filtered),
                'converted_rows': len(df_converted),
                'filter_rate': f"{len(df_filtered)/len(df_original)*100:.2f}%",
                'data_reduction': f"{(1 - len(df_filtered)/len(df_original))*100:.2f}%"
            },
            'quality_metrics': {
                'category_distribution': df_filtered['mh_category'].value_counts().to_dict() if 'mh_category' in df_filtered.columns else {},
                'confidence_distribution': df_filtered['mh_confidence'].value_counts().to_dict() if 'mh_confidence' in df_filtered.columns else {},
                'high_confidence_ratio': f"{(df_filtered['mh_confidence'] == 'high').sum() / len(df_filtered) * 100:.2f}%" if 'mh_confidence' in df_filtered.columns else 'N/A'
            },
            'pipeline_compatibility': {
                'required_columns': ['post_created', 'post_text'],
                'optional_columns': ['user_id', 'label', 'mh_category'],
                'columns_present': df_converted.columns.tolist(),
                'is_compatible': all(col in df_converted.columns for col in ['post_created', 'post_text'])
            },
            'recommendations': []
        }

        # Add recommendations based on statistics
        filter_rate = len(df_filtered) / len(df_original) * 100
        if filter_rate < 1:
            report['recommendations'].append(
                "Filter rate sangat rendah (<1%). Pertimbangkan untuk menurunkan threshold_contextual atau menambah keywords."
            )
        elif filter_rate < 5:
            report['recommendations'].append(
                "Filter rate rendah (<5%). Dataset PPKM mungkin tidak banyak membahas mental health. Pertimbangkan menggunakan dataset lain yang lebih fokus pada mental health."
            )
        else:
            report['recommendations'].append(
                f"Filter rate baik ({filter_rate:.2f}%). Dataset siap digunakan untuk analisis."
            )

        if 'mh_confidence' in df_filtered.columns:
            high_conf_ratio = (df_filtered['mh_confidence'] == 'high').sum() / len(df_filtered) * 100
            if high_conf_ratio < 30:
                report['recommendations'].append(
                    f"Hanya {high_conf_ratio:.2f}% data dengan confidence tinggi. Pertimbangkan review manual atau fine-tuning threshold."
                )

        # Save report
        report_path = os.path.join(self.data_dir, 'preprocessing_report.json')
        with open(report_path, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2, ensure_ascii=False)

        print(f"✓ Laporan preprocessing disimpan ke {report_path}")

        # Print summary
        print("\n" + "="*60)
        print("SUMMARY PREPROCESSING")
        print("="*60)
        print(f"\nData Original: {report['statistics']['original_rows']:,} baris")
        print(f"Data Filtered: {report['statistics']['filtered_rows']:,} baris ({report['statistics']['filter_rate']})")
        print(f"Data Converted: {report['statistics']['converted_rows']:,} baris")
        print(f"\nPipeline Compatible: {'✓ YA' if report['pipeline_compatibility']['is_compatible'] else '✗ TIDAK'}")

        if report['recommendations']:
            print("\nRekomendasi:")
            for i, rec in enumerate(report['recommendations'], 1):
                print(f"  {i}. {rec}")


