# Konstanta untuk mapping sentiment PPKM dataset
# 0 = Netral, 1 = Positif, 2 = Negatif
SENTIMENT_MAP = {
    0: 'Netral',
    1: 'Positif',
    2: 'Negatif'
}


class PPKMPreprocessor:
    """
    Preprocessor untuk dataset PPKM agar siap untuk analisis mental health.
    """

    def __init__(self, data_dir='data'):
        """
        Inisialisasi PPKM Preprocessor.

        Args:
            data_dir (str): Direktori untuk menyimpan data
        """
        self.data_dir = data_dir
        os.makedirs(data_dir, exist_ok=True)

        print("="*60)
