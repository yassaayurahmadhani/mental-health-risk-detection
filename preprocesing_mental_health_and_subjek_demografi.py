def analyze_subject_category(user: str, text: str) -> Dict:
    """
    Analisis kategori subjek (User Demographics) berdasarkan username dan konten tweet.

    Returns:
        dict: {
            'subject_category': str (REMAJA, DEWASA, INDIVIDU_UMUM, MEDIA_BOT),
            'confidence': str,
            'details': str
        }
    """
    user = str(user).lower()
    text = str(text).lower()

    # 1. Cek Non-Individu berdasarkan Username
    for kw in NON_INDIVIDUAL_KEYWORDS:
        if kw in user:
            return {
                'subject_category': 'MEDIA_BOT',
                'subject_confidence': 'HIGH',
                'subject_details': f'Username keyword: {kw}'
            }

    # 2. Cek Indikator Konten
    teen_matches = [w for w in TEEN_STRONG_INDICATORS if w in text]
    adult_matches = [w for w in ADULT_STRONG_INDICATORS if w in text]

    teen_score = len(teen_matches)
    adult_score = len(adult_matches)

    category = 'INDIVIDU_UMUM'
    confidence = 'LOW'
    details = f"T:{teen_score}, A:{adult_score}"

    if teen_score == 0 and adult_score == 0:
        # Cek slang umum untuk membedakan dengan bot/formal
        general_slang = sum(1 for w in GENERAL_SLANG_NEUTRAL if w in text)
        if general_slang > 0:
            category = 'INDIVIDU_UMUM' # Informal tapi umum
        else:
            category = 'INDIVIDU_UMUM' # Netral

    elif teen_score > adult_score:
        category = 'REMAJA'
        confidence = 'MEDIUM' if teen_score < 3 else 'HIGH'
        details = f"Indicators: {','.join(teen_matches[:3])}"

    elif adult_score > teen_score:
        category = 'DEWASA'
        confidence = 'MEDIUM' if adult_score < 3 else 'HIGH'
        details = f"Indicators: {','.join(adult_matches[:3])}"

    else: # Score sama
        category = 'INDIVIDU_CAMPURAN'

    return {
        'subject_category': category,
        'subject_confidence': confidence,
        'subject_details': details
    }


class MentalHealthFilter:
    """
    Kelas untuk filtering dan labeling konten kesehatan mental.

    Attributes:
        mental_health_keywords (dict): Dictionary kata kunci per kategori
        bert_model: Model BERT untuk analisis kontekstual
        bert_tokenizer: Tokenizer BERT
        threshold_keyword (float): Threshold untuk keyword matching (default: 1)
        threshold_contextual (float): Threshold untuk contextual similarity (default: 0.65)
    """

    def __init__(self,
                 bert_model_name='indolem/indobert-base-uncased',
                 threshold_keyword=1,
                 threshold_contextual=0.65):
        """
        Inisialisasi Mental Health Filter.

        Args:
            bert_model_name (str): Nama model BERT untuk bahasa Indonesia
            threshold_keyword (int): Minimum keyword match untuk dianggap relevan
            threshold_contextual (float): Threshold similarity untuk BERT (0-1)
        """
        print("="*60)
        print("INISIALISASI MENTAL HEALTH FILTER")
        print("="*60)

        self.threshold_keyword = threshold_keyword
        self.threshold_contextual = threshold_contextual

        # 1. Load Mental Health Keywords
        print("\n1. Memuat kata kunci kesehatan mental...")
        self.mental_health_keywords = load_mental_health_keywords()
        print(f"✓ Total {sum(len(v) for v in self.mental_health_keywords.values())} kata kunci dimuat")

        # 2. Load BERT Model untuk contextual understanding
        print("\n2. Memuat model BERT untuk analisis kontekstual...")
        if not BERT_AVAILABLE:
            print("  ⚠ BERT dependencies tidak tersedia")
            print("  Menggunakan keyword matching saja tanpa contextual analysis")
            self.bert_model = None
            self.bert_tokenizer = None
        else:
            try:
                self.bert_tokenizer = AutoTokenizer.from_pretrained(bert_model_name)
                self.bert_model = AutoModel.from_pretrained(bert_model_name)
                self.bert_model.eval()  # Set ke evaluation mode
                print(f"✓ Model BERT '{bert_model_name}' berhasil dimuat")
            except Exception as e:
                print(f"⚠ Gagal memuat BERT: {e}")
                print("  Menggunakan keyword matching saja tanpa contextual analysis")
                self.bert_model = None
                self.bert_tokenizer = None

        # 3. Generate reference embeddings untuk setiap kategori
        if self.bert_model is not None:
            print("\n3. Membuat reference embeddings untuk setiap kategori...")
            self.category_embeddings = self._generate_category_embeddings()
            print(f"✓ Reference embeddings untuk {len(self.category_embeddings)} kategori dibuat")
        else:
            self.category_embeddings = {}

        print("\n✓ Mental Health Filter siap digunakan")
        print("="*60)

    def _generate_category_embeddings(self) -> Dict[str, np.ndarray]:
        """
        Generate BERT embeddings untuk setiap kategori mental health.

        Returns:
            dict: Dictionary dengan kategori sebagai key dan embedding sebagai value
        """
        if self.bert_model is None:
            return {}

        category_embeddings = {}

        for category, keywords in self.mental_health_keywords.items():
            # Gabungkan semua keywords dalam kategori
            category_text = ' '.join(keywords)

            # Generate embedding
            embedding = self._get_bert_embedding(category_text)
            category_embeddings[category] = embedding

        return category_embeddings

    def _get_bert_embedding(self, text: str) -> np.ndarray:
        """
        Dapatkan BERT embedding untuk sebuah teks.

        Args:
            text (str): Teks yang akan di-embed

        Returns:
            np.ndarray: Vector embedding (768 dimensi untuk BERT base)
        """
        if self.bert_model is None:
            return np.zeros(768)

        # Tokenize
        inputs = self.bert_tokenizer(
            text,
            return_tensors='pt',
            truncation=True,
            max_length=512,
            padding=True
        )

        # Generate embedding
        with torch.no_grad():
            outputs = self.bert_model(**inputs)
            # Ambil [CLS] token embedding sebagai representasi teks
            # Use squeeze(0) to only remove the batch dimension
            embedding = outputs.last_hidden_state[:, 0, :].squeeze(0).numpy()

        return embedding

    def check_keyword_match(self, text: str) -> Tuple[bool, List[str], Dict[str, int]]:
        """
        Check apakah teks mengandung kata kunci mental health.

        Args:
            text (str): Teks yang akan dicek

        Returns:
            tuple: (is_match, matched_keywords, category_counts)
                - is_match: True jika ada keyword match
                - matched_keywords: List kata kunci yang match
                - category_counts: Dict jumlah match per kategori
        """
        text_lower = text.lower()
        matched_keywords = []
        category_counts = {}

        for category, keywords in self.mental_health_keywords.items():
            matches = []
            for keyword in keywords:
                if keyword.lower() in text_lower:
                    matches.append(keyword)

            if matches:
                matched_keywords.extend(matches)
                category_counts[category] = len(matches)

        is_match = len(matched_keywords) >= self.threshold_keyword

        return is_match, matched_keywords, category_counts

    def check_contextual_relevance(self, text: str) -> Tuple[bool, str, float]:
        """
        Check relevansi kontekstual menggunakan BERT similarity.

        Args:
            text (str): Teks yang akan dicek

        Returns:
            tuple: (is_relevant, best_category, max_similarity)
                - is_relevant: True jika similarity > threshold
                - best_category: Kategori dengan similarity tertinggi
                - max_similarity: Nilai similarity tertinggi
        """
        if self.bert_model is None or not self.category_embeddings:
            return False, None, 0.0

        # Generate embedding untuk teks
        text_embedding = self._get_bert_embedding(text).reshape(1, -1)

        # Hitung similarity dengan setiap kategori
        max_similarity = 0.0
        best_category = None

        for category, cat_embedding in self.category_embeddings.items():
            cat_embedding = cat_embedding.reshape(1, -1)
            if SKLEARN_AVAILABLE:
                similarity = cosine_similarity(text_embedding, cat_embedding)[0][0]
            else:
                # Fallback to numpy dot product if sklearn not available
                text_norm = np.linalg.norm(text_embedding)
                cat_norm = np.linalg.norm(cat_embedding)

                # Avoid division by zero
                if text_norm == 0 or cat_norm == 0:
                    similarity = 0.0
                else:
                    similarity = np.dot(text_embedding.flatten(), cat_embedding.flatten()) / (
                        text_norm * cat_norm
                    )

            if similarity > max_similarity:
                max_similarity = similarity
                best_category = category

        is_relevant = max_similarity >= self.threshold_contextual

        return is_relevant, best_category, max_similarity

    def classify_mental_health_content(self, text: str) -> Dict:
        """
        Klasifikasi konten berdasarkan keyword dan contextual relevance.

        Args:
            text (str): Teks yang akan diklasifikasi

        Returns:
            dict: Hasil klasifikasi dengan struktur:
                {
                    'is_mental_health_related': bool,
                    'keyword_match': bool,
                    'matched_keywords': list,
                    'category_counts': dict,
                    'contextual_match': bool,
                    'best_category': str,
                    'contextual_score': float,
                    'final_category': str,
                    'confidence': str
                }
        """
        # 1. Keyword matching
        keyword_match, matched_keywords, category_counts = self.check_keyword_match(text)

        # 2. Contextual relevance
        contextual_match, best_category_ctx, contextual_score = self.check_contextual_relevance(text)

        # 3. Determine final classification
        is_mental_health_related = keyword_match or contextual_match

        # Tentukan kategori final
        final_category = None
        confidence = 'low'

        if keyword_match and contextual_match:
            # Kedua metode setuju - confidence tinggi
            # Pilih kategori dengan keyword count tertinggi
            if category_counts:
                final_category = max(category_counts, key=category_counts.get)
            else:
                final_category = best_category_ctx
            confidence = 'high'
        elif keyword_match:
            # Hanya keyword match
            if category_counts:
                final_category = max(category_counts, key=category_counts.get)
            confidence = 'medium'
        elif contextual_match:
            # Hanya contextual match
            final_category = best_category_ctx
            confidence = 'medium'

        return {
            'is_mental_health_related': is_mental_health_related,
            'keyword_match': keyword_match,
            'matched_keywords': matched_keywords,
            'category_counts': category_counts,
            'contextual_match': contextual_match,
            'best_category_contextual': best_category_ctx,
            'contextual_score': float(contextual_score),
            'final_category': final_category,
            'confidence': confidence
        }

    def filter_mental_health_content(self,
                                     df: pd.DataFrame,
                                     text_column: str = None,
                                     user_column: str = None,
                                     target_subject_group: str = None,
                                     batch_size: int = 100) -> pd.DataFrame:
        """
        Filter dataset untuk mengambil hanya konten mental health.

        Args:
            df (pd.DataFrame): DataFrame input
            text_column (str): Nama kolom yang berisi teks (auto-detect jika None)
            user_column (str): Nama kolom username (auto-detect jika None)
            target_subject_group (str): Filter subjects ('REMAJA', 'DEWASA', 'MEDIA_BOT', etc.)
            batch_size (int): Batch size untuk processing (default: 100)

        Returns:
            pd.DataFrame: DataFrame yang sudah difilter dengan kolom tambahan
        """
        print("\n" + "="*60)
        print("FILTERING MENTAL HEALTH CONTENT")
        print("="*60)

        print(f"\n1. Data dimuat: {len(df):,} baris")

        # Auto-detect text column
        if text_column is None:
            possible_cols = ['tweet', 'Tweet', 'text', 'content', 'post_text', 'post']
            for col in possible_cols:
                if col in df.columns:
                    text_column = col
                    break

            if text_column is None:
                raise ValueError(f"Kolom teks tidak ditemukan. Kolom tersedia: {df.columns.tolist()}")

        # Auto-detect user column
        if user_column is None:
            possible_user_cols = ['user', 'User', 'username', 'screen_name', 'author']
            for col in possible_user_cols:
                if col in df.columns:
                    user_column = col
                    break

            if user_column is None:
                # Create dummy user column if not found
                print("⚠ Kolom user tidak ditemukan. Menggunakan 'unknown'.")
                df['temp_user'] = 'unknown'
                user_column = 'temp_user'

        print(f"✓ Menggunakan kolom teks: '{text_column}'")
        print(f"✓ Menggunakan kolom user: '{user_column}'")

        # Clean missing values
        df = df.dropna(subset=[text_column]).reset_index(drop=True)
        print(f"✓ Setelah menghapus missing values: {len(df):,} baris")

        # =========================================================================
        # 1.5 SUBJECT DEMOGRAPHIC ANALYSIS (PRE-FILTERING)
        # =========================================================================
        print("\n2. Melakukan Analisis Demografi Subjek...")

        subject_results = []
        for idx, row in df.iterrows():
            subj_res = analyze_subject_category(row[user_column], row[text_column])
            subject_results.append(subj_res)

        # Add subject columns
        df['subject_category'] = [r['subject_category'] for r in subject_results]
        df['subject_confidence'] = [r['subject_confidence'] for r in subject_results]
        df['subject_details'] = [r['subject_details'] for r in subject_results]

        # Display subject breakdown
        subj_counts = df['subject_category'].value_counts()
        print("\n   Distribusi Subjek:")
        for cat, count in subj_counts.items():
            print(f"   - {cat}: {count:,} ({count/len(df)*100:.1f}%)")

        # =========================================================================
        # STRICT SUBJECT FILTER (HANYA 4 KATEGORI VALID)
        # =========================================================================

        VALID_SUBJECTS = [
            'INDIVIDU_UMUM',
            'INDIVIDU_CAMPURAN',
            'REMAJA',
            'DEWASA'
        ]

        # Hapus semua selain 4 kategori resmi
        before_clean = len(df)
        df = df[df['subject_category'].isin(VALID_SUBJECTS)].reset_index(drop=True)

        print(f"\n✓ Menghapus non-individu (MEDIA_BOT, dll): {before_clean:,} -> {len(df):,} baris")

        # Jika user meminta filter spesifik
        if target_subject_group:
            original_len = len(df)
            print(f"\nCTX: Menerapkan filter subjek utama: '{target_subject_group}'")

            if target_subject_group not in VALID_SUBJECTS:
                print("⚠ Kategori tidak valid!")
                print(f"Gunakan salah satu dari: {VALID_SUBJECTS}")
                return df

            df = df[df['subject_category'] == target_subject_group].reset_index(drop=True)

            print(f"✓ Filter diterapkan: {original_len:,} -> {len(df):,} baris")

            if len(df) == 0:
                print("⚠ Peringatan: Hasil filter kosong!")
                return df

        # =========================================================================
        # END SUBJECT ANALYSIS
        # =========================================================================

        # Process in batches
        print(f"\n3. Memproses konten (Mental Health Check) (batch size: {batch_size})...")
        results = []

        total_batches = (len(df) + batch_size - 1) // batch_size

        for i in range(0, len(df), batch_size):
            batch_num = i // batch_size + 1
            batch = df.iloc[i:i+batch_size]

            print(f"   Batch {batch_num}/{total_batches} ({len(batch)} baris)...", end='\r')

            for idx, row in batch.iterrows():
                text = str(row[text_column])
                classification = self.classify_mental_health_content(text)
                results.append(classification)

        print(f"\n✓ Selesai memproses {len(results):,} baris")

        # Add classification results to dataframe
        print("\n4. Menambahkan hasil klasifikasi ke dataframe...")
        df['mh_is_related'] = [r['is_mental_health_related'] for r in results]
        df['mh_keyword_match'] = [r['keyword_match'] for r in results]
        df['mh_matched_keywords'] = [','.join(r['matched_keywords'][:MAX_KEYWORDS_TO_STORE]) for r in results]
        df['mh_contextual_match'] = [r['contextual_match'] for r in results]
        df['mh_contextual_score'] = [r['contextual_score'] for r in results]
        df['mh_category'] = [r['final_category'] for r in results]
        df['mh_confidence'] = [r['confidence'] for r in results]

        # Filter hanya yang related
        df_filtered = df[df['mh_is_related'] == True].reset_index(drop=True)

        print(f"✓ Kolom klasifikasi ditambahkan")
        print(f"\n5. Hasil Filtering:")
        print(f"   - Total data subjek terpilih: {len(df):,} baris")
        print(f"   - Data mental health: {len(df_filtered):,} baris ({len(df_filtered)/len(df)*100:.2f}%)")
        print(f"   - Data non-mental health: {len(df) - len(df_filtered):,} baris")

        # Statistics
        if len(df_filtered) > 0:
            print(f"\n6. Distribusi Kategori (Mental Health):")
            category_dist = df_filtered['mh_category'].value_counts()
            for cat, count in category_dist.items():
                print(f"   - {cat}: {count:,} ({count/len(df_filtered)*100:.2f}%)")

            print(f"\n7. Distribusi Confidence:")
            conf_dist = df_filtered['mh_confidence'].value_counts()
            for conf, count in conf_dist.items():
                print(f"   - {conf}: {count:,} ({count/len(df_filtered)*100:.2f}%)")

        print("\n" + "="*60)

        return df_filtered

    def save_filtered_data(self, df: pd.DataFrame, output_path: str):
        """
        Simpan data yang sudah difilter.

        Args:
            df (pd.DataFrame): DataFrame yang akan disimpan
            output_path (str): Path output file
        """
        print(f"\nMenyimpan data ke {output_path}...")
