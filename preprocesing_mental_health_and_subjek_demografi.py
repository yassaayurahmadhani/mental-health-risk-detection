    def generate_filter_report(self, df_original: pd.DataFrame,
                              df_filtered: pd.DataFrame,
                              output_path: str = 'mental_health_filter_report.json'):
        """
        Generate laporan filtering.

        Args:
            df_original (pd.DataFrame): DataFrame asli
            df_filtered (pd.DataFrame): DataFrame yang sudah difilter
            output_path (str): Path untuk menyimpan laporan
        """
        report = {
            'filter_metadata': {
                'timestamp': datetime.now().isoformat(),
                'threshold_keyword': self.threshold_keyword,
                'threshold_contextual': self.threshold_contextual,
                'bert_model': 'indolem/indobert-base-uncased' if self.bert_model else None
            },
            'statistics': {
                'total_original': len(df_original),
                'total_filtered': len(df_filtered),
                'filter_rate': f"{len(df_filtered)/len(df_original)*100:.2f}%"
            },
            'category_distribution': df_filtered['mh_category'].value_counts().to_dict() if len(df_filtered) > 0 else {},
            'confidence_distribution': df_filtered['mh_confidence'].value_counts().to_dict() if len(df_filtered) > 0 else {},
            'method_performance': {
                'keyword_only': int((df_filtered['mh_keyword_match'] & ~df_filtered['mh_contextual_match']).sum()),
                'contextual_only': int((~df_filtered['mh_keyword_match'] & df_filtered['mh_contextual_match']).sum()),
                'both_methods': int((df_filtered['mh_keyword_match'] & df_filtered['mh_contextual_match']).sum())
            }
        }

        with open(output_path, 'w', encoding='utf-8') as f:
            json.dump(report, f, indent=2, ensure_ascii=False)

        print(f"\n✓ Laporan filtering disimpan ke {output_path}")

        return report


